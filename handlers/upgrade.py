MILITARY_BUILDINGS = ["کمپ شمشیرزن","کمپ کماندار","کمپ نیزه‌دار","کمپ سوارکار","کمپ نیروی ویژه","پادگان"]

def get_guide(name):
    if name == "قلعه": return CASTLE_GUIDE
    if name in MILITARY_BUILDINGS: return MILITARY_GUIDE
    if name == "ساختمان ویژه": return SPECIAL_GUIDE
    return GENERAL_GUIDE

def get_income_preview(name, level, region):
    new_level = level + 1
    db_income = get_building_income()
    if name == "ساختمان ویژه":
        inc = SPECIAL_BUILDING_INCOME.get(region, {})
        if inc: return " | ".join([f"+{v*new_level} {k}/هفته" for k,v in inc.items()])
    elif name in db_income:
        parts = []
        for res, base in db_income[name].items():
            if res != "رضایت": parts.append(f"+{base*new_level} {res}/هفته")
        return " | ".join(parts)
    return ""

async def show_upgrades(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    player = get_player(update.effective_user.id)
    if not player: await query.edit_message_text("ابتدا /start بزن!"); return

    buildings = player['buildings']
    keyboard = []

    # قلعه اول
    castle_lv = buildings.get("قلعه", 1)
    if castle_lv < 15 and castle_lv in UPGRADE_COSTS.get("قلعه", {}):
        info = CASTLE_INFO.get(castle_lv+1, "")
        keyboard.append([InlineKeyboardButton(f"🏛️ قلعه | {castle_lv} ← {castle_lv+1} | {info.split('|')[1].strip() if '|' in info else ''}", callback_data="upg_قلعه")])

    for name, level in buildings.items():
        if name == "قلعه": continue
        max_lv = MAX_LEVELS.get(name, 999)
        costs = UPGRADE_COSTS.get(name, {})
        if level in costs and level < max_lv:
            emoji = BUILDING_EMOJIS.get(name, "🏠")
            preview = get_income_preview(name, level, player['region'])
            label = f"{emoji} {name} | {level} ← {level+1}"
            if preview: label += f" | {preview}"
            keyboard.append([InlineKeyboardButton(label, callback_data=f"upg_{name}")])

    keyboard.append([InlineKeyboardButton("🔙 بازگشت", callback_data="main_menu")])
    await query.edit_message_text("🏗️ *ارتقای ساختمان‌ها*\n\nانتخاب کن:", parse_mode="Markdown",
        reply_markup=InlineKeyboardMarkup(keyboard))

async def upgrade_building(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    name = query.data.replace("upg_", "")
    player = get_player(update.effective_user.id)
    if not player: return

    level = player['buildings'].get(name, 0)
    costs = UPGRADE_COSTS.get(name, {}).get(level)
    if not costs: await query.answer("❌ ارتقا ممکن نیست!", show_alert=True); return

    guide = get_guide(name)
    cost_text = " | ".join([f"{r}: {a:,}" for r,a in costs.items()])
    preview = get_income_preview(name, level, player['region'])
    emoji = BUILDING_EMOJIS.get(name, "🏠")

    msg = f"{guide}\n\n{'━'*20}\n"
    msg += f"{emoji} *ارتقای {name}*\n"
    msg += f"📊 لول: {level} ← *{level+1}*\n"
    msg += f"💸 هزینه: {cost_text}\n"
    if preview: msg += f"📈 بازدهی جدید: {preview}"

    await query.edit_message_text(msg, parse_mode="Markdown",
        reply_markup=InlineKeyboardMarkup([
            [InlineKeyboardButton("✅ تایید ارتقا", callback_data=f"upg_confirm_{name}")],
            [InlineKeyboardButton("🔙 بازگشت", callback_data="upgrade")],
        ]))

async def confirm_upgrade(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    name = query.data.replace("upg_confirm_", "")
    player = get_player(update.effective_user.id)
    if not player: return

    buildings = player['buildings']
    resources = player['resources']
    level = buildings.get(name, 0)
    costs = UPGRADE_COSTS.get(name, {}).get(level)
    if not costs: await query.answer("❌ ارتقا ممکن نیست!", show_alert=True); return

    missing = [f"{r}: {a-resources.get(r,0):,} کم داری" for r,a in costs.items() if resources.get(r,0) < a]
    if missing: await query.answer("❌ منابع کافی نیست:\n" + "\n".join(missing), show_alert=True); return

    for r, a in costs.items():
        resources[r] = resources.get(r,0) - a
    buildings[name] = level + 1
    update_player(update.effective_user.id, "resources", resources)
    update_player(update.effective_user.id, "buildings", buildings)

    emoji = BUILDING_EMOJIS.get(name, "🏠")
    preview = get_income_preview(name, level, player['region'])
    msg = f"✅ *{emoji} {name}* ارتقا یافت!\n📈 لول: {level} ← *{level+1}*"
    if preview: msg += f"\n📊 بازدهی جدید: {preview}"

    await query.edit_message_text(msg, parse_mode="Markdown",
        reply_markup=InlineKeyboardMarkup([[InlineKeyboardButton("🔙 بازگشت", callback_data="upgrade")]]))
